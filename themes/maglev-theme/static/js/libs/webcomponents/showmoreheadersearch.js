var dataSize = data.body.length;
var resultsPerPage = 10;
var MaxChunks = Math.floor(dataSize/resultsPerPage)+1

class ShowMoreHeaderSearchSample extends React.Component {
    constructor() {
        super();
        this.onClickRow = this.onClickRow.bind(this);
        this._onMore = this._onMore.bind(this);
        this._onRefresh = this._onRefresh.bind(this);
        this._onExport = this._onExport.bind(this);
        this._onAdd = this._onAdd.bind(this);
        this.state = { data: { headers: data.headers, body: data.body.slice(0,10) }, dataSize: dataSize }
    }
    onClickRow(event){
        alert('React just got an event for row number: ' + event.detail.ClickRow);
    }
    _onMore(event){
        const n = event.detail.currentChunk;
        if (n > MaxChunks) {
            console.log('This is the end of the dataset');
        } else {
            this.setState({ data: { headers: data.headers, body: data.body.slice(0,n*10) }});
        }
    }
    _onRefresh(event) {
        alert('refreshing ...');
    }
    _onExport(event) {
        alert('exporting...');
    }
    _onAdd(event){
        alert('adding...');
    }
    render() {
        var Tablename = 'Table Name';
        var Lastupdated = 'Last Updated 10 min ago';
        var Placeholder = 'Find';
        var onMore = this._onMore;
        var onClick = this.onClickRow;
        var onRefresh = this._onRefresh;
        var onExport = this._onExport;
        var onAdd = this._onAdd;
        var selectable = true;
        var data = this.state.data;
        var dataBody = this.state.data.body;
        var dataSize = this.state.dataSize;
        var properties = { onMore, onClick, onRefresh, onExport, onAdd, selectable, data, dataBody, dataSize, Tablename, Lastupdated, Placeholder };
        return DnxTableShowMoreHeaderSearch(properties);
    }
}

ReactDOM.render(
  React.createElement(ShowMoreHeaderSearchSample, null, null),
  document.getElementById('react-app')
); 